    // Form submission handler.
    $('#vaccine-form').submit(function (event) {
        event.preventDefault(); // Prevents form from submitting the traditional way
        let city = $('#city').val();
        let state = $('#state').val();
        let zip = $('#zip').val();
        let insurance_accepted = $('#insurance').is(':checked');
        let walkins_accepted = $('#walkins').is(':checked');
        let in_stock = $('#in_stock').is(':checked');
        let offers_mask = $('#offers_mask').is(':checked');
        let selectedDate = $('#date').datepicker('getDate');
        let dayOfWeek = selectedDate ? selectedDate.getDay() : null;

        // Creat the query URL for the CDC API.
        let url = 'https://data.cdc.gov/resource/5jp2-pgaw.json?';

        // Append query parameters based on the form data.
        if (city) {
            city = city.replaceAll(' ', '%20');
            url += 'loc_admin_city=' + city + "&";
        }
        if (state) {
            url += 'loc_admin_state=' + state + "&";
        }
        if (insurance_accepted) {
            url += 'insurance_accepted=' + insurance_accepted + "&";
        }
        if (walkins_accepted) {
            url += 'walkins_accepted=' + walkins_accepted + "&";
        }
        if (in_stock) {
            url += 'in_stock=' + in_stock + "&";
        }
        if (offers_mask) {
            url += 'offers_free_masks=' + offers_mask;
        }

        // Fetch data from the CDC API and process it.
        $.getJSON(url, function (data) {
            console.log(data); // For debuging.
            // Clear existing markers array.
            markers.forEach(function (marker) { marker.setMap(null); });
            markers = [];

            // Iterate over the data to create Google Map markers.
            data.forEach(function (item) {
                if (item.latitude && item.longitude) {
                    // Create and place a marker for each item from the search result.
                    let markerLocation = new google.maps.LatLng(item.latitude, item.longitude);
                    let marker = new google.maps.Marker({
                        position: markerLocation,
                        map: map,
                        title: item.loc_name
                    });
                    // Add click listener to each marker.
                    google.maps.event.addListener(marker, 'click', function () {
                        // Set content and open the InfoWindow.
                        infowindow.setContent(`<p><b>${item.loc_name}</b></p>
                                    <p><b>Address:&nbsp</b>
                                    ${item.loc_admin_street1}, 
                                    ${item.loc_admin_city}, 
                                    ${item.loc_admin_state}
                                    </p>
                                    ${item.web_address ? `<p><b>Store details:</b> <a href="${item.web_address}" target="_blank">${item.web_address}</a></p>` : ''}
                                    ${item.pre_screen ? `<p><b>Pre-screen:</b> <a href="${item.pre_screen}" target="_blank">${item.pre_screen}</a></p>` : ''}
                                    ${item.provider_notes ? `<p><b>Provider notes:&nbsp</b>${item.provider_notes}</p>` : ''}`);
                        infowindow.open(map, marker);
                    });
                    markers.push(marker);// Add the marker to the markers array.
                };
            });

            // Initialize or update the DataTable.
            if ($.fn.DataTable.isDataTable('#vaccine-table')) {
                $('#vaccine-table').DataTable().clear().destroy(); // Reset the existing DataTable completely. 
            }

            // Filter data based on zip code for partial matching.
            if (zip) {
                // The data is filtered so that only items with a zip code starting with the entered value are included.
                data = data.filter(function (item) {
                    return item.loc_admin_zip && item.loc_admin_zip.startsWith(zip); // Ensure that the startsWith method is not called on a null or undefined value.
                });
            }

            // Show opening hours for a specific day of the week.
            if (dayOfWeek !== null) { // Check if a specific day of the week is selected.
                // Array representing the names of the days columns.
                let dayList = ['sunday_hours', 'monday_hours', 'tuesday_hours', 'wednesday_hours', 'thursday_hours', 'friday_hours', 'saturday_hours'];
                // Select the relevant day's column name based on the selected day of the week.
                let dayColumn = dayList[dayOfWeek];

                // Map over the data to add a new 'opening_hours' property.
                data = data.map(function (item) {
                    return {
                        ...item, // Keep all existing properties of the item.
                        opening_hours: item[dayColumn] || 'No Available Data' // Add 'opening_hours' based on the selected day or 'Not Available' if null.
                    };
                });
            }

            // Initialize DataTables with the fetched data.
            $('#vaccine-table').DataTable({
                "data": data, // Pass the fetched data to DataTables.
                "columns": [ // Define columns and map them to data object properties.
                    { "data": "loc_name" },
                    { "data": "loc_admin_street1" },
                    { "data": "loc_admin_city" },
                    { "data": "loc_admin_state" },
                    { "data": "loc_admin_zip" },
                    {
                        "data": "opening_hours",
                        "defaultContent": "Please select a date" // Default content will be "Please select a date" if no date is selected.
                    },
                    { "data": "med_name" },
                    {
                        "data": "in_stock",
                        "render": function (data, type, row) {
                            return data === true ? 'Yes' : data === false ? 'No' : 'No Available Data'; // Custom render function for 'in_stock' to display 'Yes', 'No' or 'No Available Data'.
                        }
                    },
                    {
                        "data": "offers_free_masks",
                        "render": function (data, type, row) {
                            return data === true ? 'Yes' : data === false ? 'No' : 'No Available Data';
                        }
                    },
                    {
                        "data": "walkins_accepted",
                        "render": function (data, type, row) {
                            return data === true ? 'Yes' : data === false ? 'No' : 'No Available Data';
                        }
                    },
                    {
                        "data": "insurance_accepted",
                        "render": function (data, type, row) {
                            return data === true ? 'Yes' : data === false ? 'No' : 'No Available Data';
                        }
                    }
                ],
                // DataTables pagination options.
                "paging": true,
                "pageLength": 10,
                "lengthChange": true,
                "lengthMenu": [5, 10, 25, 50, 100, 200],
                "searching": true,
                "ordering": true,
                "info": true,

                // Initialization complete callback function once the DataTable is fully drawn and initialized.
                "initComplete": function (settings, json) {
                    // Add click event listener to each row in the table body.
                    $('#vaccine-table tbody').on('click', 'tr', function () {
                        let data = $('#vaccine-table').DataTable().row(this).data();
                        // Check if the selected row's data has latitude and longitude.
                        if (data.latitude && data.longitude) {
                            // Focus the Google map on the selected location.
                            const location = new google.maps.LatLng(data.latitude, data.longitude);
                            map.setCenter(location); // Set the center of the map.
                            map.setZoom(15); // Set the zoom level.

                            // Loop through all markers to find the one matching the clicked row.
                            markers.forEach(function (marker) {
                                if (marker.getPosition().equals(location)) {
                                    // Set the content of the InfoWindow and open it.
                                    infowindow.setContent(`<p><b>${item.loc_name}</b></p>
                                    <p><b>Address:&nbsp</b>
                                    ${item.loc_admin_street1}, 
                                    ${item.loc_admin_city}, 
                                    ${item.loc_admin_state}
                                    </p>
                                    ${item.web_address ? `<p><b>Store details:</b> <a href="${item.web_address}" target="_blank">${item.web_address}</a></p>` : ''}
                                    ${item.pre_screen ? `<p><b>Pre-screen:</b> <a href="${item.pre_screen}" target="_blank">${item.pre_screen}</a></p>` : ''}
                                    ${item.provider_notes ? `<p><b>Provider notes:&nbsp</b>${item.provider_notes}</p>` : ''}`)

                                    infowindow.open(map, marker);
                                }
                            });
                        } else {
                            alert("Location coordinates not available for this store.");
                        };
                    });
                }
            });
        });
    });
